let currentLang =
  localStorage.getItem("lang") ||
  "en"; /* ja nastavím default jazyk na English, bez auto detekcie */
window.currentLang =
  currentLang; /* ja si dám jazyk aj globálne, keby ho niekde používa iný skript */

window.t = function (key) {
  return LANG[currentLang] && LANG[currentLang][key]
    ? LANG[currentLang][key]
    : key;
};

function applyTranslations() {
  /* ja prepíšem všetky texty s data-i18n */
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    /* ja prejdem všetky prekladové elementy */
    const key = el.getAttribute("data-i18n"); /* ja zoberiem názov prekladu */
    el.textContent = t(key); /* ja nastavím preložený text */
  });
}

function setLanguage(lang) {
  /* nastaví jazyk po kliknutí v menu */
  if (!LANG[lang]) return; /* stop, ak jazyk neexistuje */
  currentLang = lang; /* nastaví aktuálny jazyk */

  localStorage.setItem("lang", lang); /* uloží výber jazyka */
  window.currentLang = lang; /* sprístupní jazyk globálne */

  document.querySelectorAll("#language-menu .lang-item").forEach((b) => {
    /* prejde všetky jazykové tlačidlá */
    b.classList.remove("is-active"); /* zruší selected class */
    b.removeAttribute("aria-current"); /* zruší aria selected */
    b.removeAttribute("data-active"); /* zruší data selected */
  });

  const activeBtn = document.querySelector(
    `#language-menu .lang-item[data-lang="${lang}"]`,
  ); /* nájde tlačidlo pre zvolený jazyk */
  if (activeBtn) {
    /* ak existuje */
    activeBtn.classList.add("is-active"); /* nastaví selected class */
    activeBtn.setAttribute("aria-current", "true"); /* nastaví aria selected */
    activeBtn.setAttribute("data-active", "true"); /* nastaví data selected */
  }

  applyTranslations(); /* prepíše texty podľa jazyka */

  const menu = document.getElementById("language-menu"); /* nájde menu */
  if (menu) menu.classList.add("hidden"); /* zavrie menu po výbere */
}

document.addEventListener("DOMContentLoaded", () => {
  /* počká na HTML */
  const btn = document.getElementById("lang-btn"); /* language ikona */
  const menu =
    document.getElementById("language-menu"); /* existujúce menu z HTML */
  if (!btn || !menu) return; /* stop ak niečo chýba */

  btn.addEventListener("click", (e) => {
    /* otvorenie menu */
    e.stopPropagation(); /* zabráni zatvoreniu */
    menu.classList.toggle("hidden"); /* prepne menu */
  });

  document.addEventListener("click", (e) => {
    /* klik mimo */
    if (!menu.contains(e.target) && !btn.contains(e.target)) {
      menu.classList.add("hidden"); /* zavrie menu */
    }
  });

  menu.querySelectorAll(".lang-item").forEach((b) => {
    /* všetky jazyky */
    b.addEventListener("click", () => {
      const lang = b.getAttribute("data-lang"); /* kód jazyka */
      setLanguage(lang); /* nastav jazyk */
    });
  });

  setLanguage(currentLang); /* nastav default + highlight */
});

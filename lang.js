function tCountry(name) {
  /* ja preložím názov krajiny podľa aktuálneho jazyka */
  const lang =
    window.currentLang || localStorage.getItem("lang") || "en"; /* ja zistím jazyk */

  if (typeof LANG === "undefined" || !LANG[lang]) return name; /* ja sa poistím */
  const dict = LANG[lang].countries || {}; /* ja zoberiem slovník krajín */
  return dict[name] || name; /* ja vrátim preklad, alebo originál */
}

